# Valor absoluto de números inteiros
print("%d" % abs(-1))               # =   1 
print("%d" % abs(1))                # =   1 
 
# Valor binário
print(bin(10))                      # =   1010 
print(bin(15))                      # =   1111 
 
# Valor hexadecimal
print(hex(10))                      # =   A 
print(hex(15))                      # =   F 
 
# Valor octal
print(oct(10))                      # =   A 
print(oct(15))                      # =   F 

# Arredondamento de valor numérico
print("%.8f" % round(25.234567, 2)) # =   3.1415926536 
print("%.8f" % round(25.234567, 3)) # =   3.1415926536 

pausa()
